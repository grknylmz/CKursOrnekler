int reverse(int val)
{
	int retval = 0;

	while (val != 0) {
		retval = retval * 10 + val % 10;
		val /= 10;
	}

	return retval;
}

int sumdigit(int val)
{
	int sum = 0;

	while (val != 0) {
		sum += val % 10;
		val /= 10;
	}

	return sum;
}


int ndigit(int val)
{
	int digitCounter = 0;

	if (val == 0)
		return 1;

	while (val != 0) {
		digitCounter++;
		val /= 10;
	}

	return digitCounter;
}



int main()
{
	int x;

	printf("bir tamsayi girin : ");
	scanf("%d", &x);

	printf("%d sayisi %d basamakli\n", x, ndigit(x));
	printf("%d sayisi basamaklari toplami %d\n", x, sumdigit(x));
	printf("%d sayisi tersi %d\n", x, reverse(x));


	return 0;
}
