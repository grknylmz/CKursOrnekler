void swap(int *p1, int *p2)
{
	int temp = *p1;
	*p1 = *p2;
	*p2 = temp;
}


int *reverseArray(int *ptr, int size)
{
	int *ptemp = ptr;
	int *pe = ptr + size;

	while (ptr < pe)
		swap(ptr++, --pe);

	return ptemp;
}


void delay(double sec)
{
	clock_t start = clock();

	while ((double)(clock() - start) / CLOCKS_PER_SEC < sec)
		;
}




