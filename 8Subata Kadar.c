char *mystrchr(const char *p, int ch)
{
	while (*p) {
		if (*p == ch)
			return (char *)p;
		p++;
	}

	if (ch == '\0')
		return (char *)p;

	return NULL;
}

char *mystrchr(const char *p, int ch)
{
	int k;

	for (k = 0; p[k] != '\0'; k++)
		if (p[k] == ch)
			return (char *)(p + k);

	if (ch == '\0')
		return (char *)(p + k);
	
	return NULL;
}

