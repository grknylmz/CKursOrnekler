int mystricmp(const char *p1, const char *p2)
{
	while (toupper(*p1) == toupper(*p2)) {
		if (*p1 == '\0')
			return 0;
		p1++, p2++;
	}

	return toupper(*p1) - toupper(*p2);

}

int mystrcmp(const char *p1, const char *p2)
{
	while (*p1 == *p2) {
		if (*p1 == '\0')
			return 0;
		p1++, p2++;
	}

	return *p1 - *p2;

}


